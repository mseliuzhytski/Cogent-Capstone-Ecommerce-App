package com.cogent.ecommerce.service;

public class FilenameObject {

    private String filename;

    private String abbreviatedFilename;

    public String getFilename() {
        return filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }

    public String getAbbreviatedFilename() {
        return abbreviatedFilename;
    }

    public void setAbbreviatedFilename(String abbreviatedFilename) {
        this.abbreviatedFilename = abbreviatedFilename;
    }
}
